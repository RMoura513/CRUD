package controller;

public class Curso {
	
	private Long id;
	private String nome;
	private Long cod;
	private String nomeCoor;
	private int qtdAl;
	
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public Long getCod() {
		return cod;
	}
	public void setCod(Long cod) {
		this.cod = cod;
	}
	public String getNomeCoor() {
		return nomeCoor;
	}
	public void setNomeCoor(String nomeCoor) {
		this.nomeCoor = nomeCoor;
	}
	public int getQtdAl() {
		return qtdAl;
	}
	public void setQtdAl(int qtdAl) {
		this.qtdAl = qtdAl;
	}
	

}
